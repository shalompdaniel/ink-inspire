    <h2>Aftercare Instructions</h2>
    <ul>
        <li>Keep the bandage or wrap on your tattoo for the first few hours as instructed by your tattoo artist.</li>
        <li>After removing the bandage, gently wash the tattoo with lukewarm water and mild, unscented soap.</li>
        <li>Pat the tattoo dry with a clean, soft towel or paper towel. Avoid rubbing.</li>
        <li>Apply a thin layer of fragrance-free healing ointment or tattoo aftercare lotion.</li>
    </ul>

    <h2>Daily Care</h2>
    <ul>
        <li>Clean the tattoo gently 2-3 times a day using mild soap and water.</li>
        <li>Apply a thin layer of aftercare lotion or ointment as needed, usually 2-3 times a day.</li>
        <li>Avoid soaking the tattoo in water (e.g., swimming, long baths) for at least 2 weeks.</li>
        <li>Avoid direct sunlight and tanning beds on the tattooed area. Use sunscreen once healed.</li>
        <li>Wear loose, clean clothing over the tattoo to avoid irritation.</li>
    </ul>

    <h2>What to Avoid</h2>
    <ul>
        <li>Do not pick at scabs or peeling skin, as this can cause scarring or color loss.</li>
        <li>Avoid applying perfumes, fragrances, or harsh chemicals to the tattooed area.</li>
        <li>Refrain from excessive sweating or strenuous exercise that can cause friction or irritation.</li>
    </ul>

    <h2>Healing Process</h2>
    <ul>
        <li>The tattoo will go through a healing process including redness, swelling, and scabbing. This is normal.</li>
        <li>If you notice signs of infection (increased redness, swelling, pus, or pain), contact your tattoo artist or a healthcare professional.</li>
        <li>Most tattoos take about 2-4 weeks to heal on the surface, but full healing can take a few months.</li>
    </ul>
